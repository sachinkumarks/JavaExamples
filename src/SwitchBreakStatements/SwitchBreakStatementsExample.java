package SwitchBreakStatements;

public class SwitchBreakStatementsExample {

	public static void main(String[] args) {

		String month="jan";
		switch(month) {
		case "jan": System.out.println("welcome to 1st month of "+month);
		break;
		case "feb": System.out.println("welcome to 2nd month of "+month);
		break;
		case "mar": System.out.println("welcome to 1st month of "+month);
		break;
		case "april": System.out.println("welcome to 1st month of "+month);
		break;
		case "may": System.out.println("welcome to 1st month of "+month);
		break;
		case "jun": System.out.println("welcome to 1st month of "+month);
		break;
		case "july": System.out.println("welcome to 1st month of "+month);
		break;
		case "aug": System.out.println("welcome to 1st month of "+month);
		break;
		case "sept": System.out.println("welcome to 1st month of "+month);
		break;
		case "oct": System.out.println("welcome to 1st month of "+month);
		break;
		case "nov": System.out.println("welcome to 1st month of "+month);
		break;
		case "dec": System.out.println("welcome to 1st month of "+month);
		break;
        default : System.out.println("none of the above condition is not satisfied");
		
		}
	}

}
